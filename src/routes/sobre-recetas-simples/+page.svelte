	<h1>Recetas Simples</h1>
	<p>
		<strong>Hola!</strong> Mi nombre es Luz Ojeda y soy la creadora de la página. Empezó como una idea en
		diciembre de 2023 después de unos meses de que empecé a aprender cocina más "seriamente" con el libro
		The Food Lab de J. Kenji López-Alt. Durante toda mi vida disfruté cocinar pero nunca me tomé el tiempo
		y la atención necesaria para aprender los por qué y cómos detrás de esta actividad que para algunos
		es un hobbie, para otros un trabajo, para ambos puede ser un arte, y para otros tantos todo a la
		vez y una pasión.
	</p>
	<p>
		De no encontrarme satisfecha con la mayoría de las páginas web de cocina en español que hay
		actualmente decidí combinar dos actividades que me apasionan como es el desarrollo web y la
		cocina y hacer una propia página de recetas. Una que no tuviera demasiada publicidad invasiva,
		fuera sencilla en diseño y tuviera la información justa y necesaria en cada receta.
	</p>
	<p>
		La página es actualmente un repositorio sencillo de recetas libres que encontré en internet y
		traduje al español manualmente. Pero el plan a futuro es permitir la creación de recetas por
		parte de usuarios.
	</p>
	<p>
		Cualquier sugerencia, consejo o apreciación es más que bienvenida en <strong
			>recetassimples@proton.me</strong
		>!
	</p>
